public class conditionals {
    public static void main(String[] args) {
        for (int i =0;i<6;i+=1){
            System.out.print(i+" ");
        }


        int num=0;
        while (num<=10){
            System.out.println(num);
            num+=1;
        }

        // do while 
        do {
            System.out.println("hello world");
        } while (num==0);


    }
}
